    private void botOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botOKActionPerformed
        int codigoT;
        String nombres;
        double numHoras, ret, valorHora, salarioBto, salarioNto;
        try {
            codigoT = Integer.parseInt(codigo.getText());
            nombres = Nombre.getText();
            numHoras = Double.parseDouble(horasT.getText());
            ret = Double.parseDouble(retencion.getText());
            valorHora = Double.parseDouble(valorH.getText());
            
            salarioBto = calcularSalario.Salariobruto(valorHora, numHoras);
            salarioNto = calcularSalario.Salarioneto(valorHora, numHoras, ret);

            JOptionPane.showMessageDialog(null,"C칩digo: " + codigoT + "\n" + "Nombre: " + nombres + "\n" + "Salario bruto: " + salarioBto + "\n" + "Salario neto: " + salarioNto);
        }
        catch (NumberFormatException ex){
            JOptionPane.showMessageDialog(null,"Datos inv치lidos.");
        }
    }//GEN-LAST:event_botOKActionPerformed

